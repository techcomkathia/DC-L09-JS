let turno = 'Noite'


switch (turno) {
    case 'noite':
        console.log('boa noite')
        break;
    case 'tarde':
        console.log ('boa tarde')
        break;
    case 'manha':
        console.log('bom dia')
        break;
    default:
        alert('turno desconhecido')
        break;
}


